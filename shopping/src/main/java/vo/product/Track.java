package vo.product;

public class Track {
	private int productTrackNo;
	private int productNo;
	private int trackNo;
	private String trackName;
	private String productSinger;
	private int trackTime;
	private String createdate;
	private String updatedate;
	public int getProductTrackNo() {
		return productTrackNo;
	}
	public void setProductTrackNo(int productTrackNo) {
		this.productTrackNo = productTrackNo;
	}
	public int getProductNo() {
		return productNo;
	}
	public void setProductNo(int productNo) {
		this.productNo = productNo;
	}
	public int getTrackNo() {
		return trackNo;
	}
	public void setTrackNo(int trackNo) {
		this.trackNo = trackNo;
	}
	public String getTrackName() {
		return trackName;
	}
	public void setTrackName(String trackName) {
		this.trackName = trackName;
	}
	public String getProductSinger() {
		return productSinger;
	}
	public void setProductSinger(String productSinger) {
		this.productSinger = productSinger;
	}
	public int getTrackTime() {
		return trackTime;
	}
	public void setTrackTime(int trackTime) {
		this.trackTime = trackTime;
	}
	public String getCreatedate() {
		return createdate;
	}
	public void setCreatedate(String createdate) {
		this.createdate = createdate;
	}
	public String getUpdatedate() {
		return updatedate;
	}
	public void setUpdatedate(String updatedate) {
		this.updatedate = updatedate;
	}
	
}
